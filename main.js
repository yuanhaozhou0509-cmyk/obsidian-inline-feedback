  // ---- Private helpers ----
  handleSelectionChange() {
    const activeView = this.app.workspace.getActiveViewOfType(import_obsidian7.MarkdownView);
    if (!activeView || !activeView.file) {
      this.popup.hide();
      return;
    }
    if (activeView.file.path !== this.activeFilePath) {
      this.activeFilePath = activeView.file.path;
    }
    const editor = activeView.editor;
    const selection = editor.getSelection();
    if (selection && selection.trim().length > 0) {
      const from = editor.getCursor("from");
      const to = editor.getCursor("to");
      const domSel = window.getSelection();
      if (domSel && domSel.rangeCount > 0) {
        const rect = domSel.getRangeAt(0).getBoundingClientRect();
        this.popup.show(
          rect,
          activeView.file.path,
          selection,
          from.line + 1,
          to.line + 1,
          from.ch,
          to.ch
        );
      }
    } else {
      this.popup.hide();
    }
  }
  refreshDecorations() {
    const leaves = this.app.workspace.getLeavesOfType("markdown");
    leaves.forEach((leaf) => {
      const view = leaf.view;
      try {
        const cm = view.editor.cm;
        if (cm) {
          cm.dispatch({ effects: refreshEffect.of(null) });
        }
      } catch (e) {
      }
    });
  }
  refreshFeedbackSidebar() {
    const leaves = this.app.workspace.getLeavesOfType(VIEW_TYPE_FEEDBACK);
    leaves.forEach((leaf) => {
      const view = leaf.view;
      if (view && view.refresh) {
        view.refresh();
      }
    });
  }
  refreshCardSidebar() {
    const leaves = this.app.workspace.getLeavesOfType(VIEW_TYPE_CONCEPT_CARDS);
    leaves.forEach((leaf) => {
      const view = leaf.view;
      if (view && view.refresh) {
        view.refresh();
      }
    });
  }
  navigateToAnnotation(ann) {
    const activeView = this.app.workspace.getActiveViewOfType(import_obsidian7.MarkdownView);
    if (!activeView) return;
    const editor = activeView.editor;
    const line = ann.lineStart - 1;
    const ch = ann.charStart;
    editor.setCursor({ line, ch });
    editor.scrollIntoView(
      {
        from: { line, ch },
        to: { line: ann.lineEnd - 1, ch: ann.charEnd }
      },
      true
    );
  }
  /** Open a file at a specific line number */
  async openFileAtLine(filePath, line) {
    const file = this.app.vault.getAbstractFileByPath(filePath);
    if (file && file instanceof import_obsidian7.TFile) {
      const leaf = this.app.workspace.getLeaf();
      await leaf.openFile(file);
      setTimeout(() => {
        const view = this.app.workspace.getActiveViewOfType(import_obsidian7.MarkdownView);
        if (view) {
          const editor = view.editor;
          const targetLine = Math.max(0, line - 1);
          editor.setCursor({ line: targetLine, ch: 0 });
          editor.scrollIntoView(
            { from: { line: targetLine, ch: 0 }, to: { line: targetLine, ch: 0 } },
            true
          );
        }
      }, 100);
    } else {
      new import_obsidian7.Notice(`File not found: ${filePath}`);
    }
  }
  async toggleSidebar(viewType) {
    const existing = this.app.workspace.getLeavesOfType(viewType);
    if (existing.length > 0) {
      existing.forEach((leaf2) => leaf2.detach());
      return;
    }
    const leaf = this.app.workspace.getRightLeaf(false);
    if (leaf) {
      await leaf.setViewState({ type: viewType, active: true });
      this.app.workspace.revealLeaf(leaf);
    }
  }
  async loadSettings() {
    this.settings = Object.assign({}, DEFAULT_SETTINGS, await this.loadData());
  }
  async saveSettings() {
    await this.saveData(this.settings);
    if (this.conceptCardStore) {
      this.conceptCardStore.setBasePath(this.settings.conceptCardPath);
    }
    if (this.popup) {
      this.popup.setCategories(this.settings.cardCategories || []);
    }
  }
